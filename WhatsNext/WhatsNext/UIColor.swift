import Foundation
import UIKit

extension UIColor {
    
    class var Gray:UIColor {
        return UIColor(red: 142/255, green: 142/255, blue: 146/255, alpha: 1.0)
    }
    class var OpaqueGray:UIColor {
        return UIColor(red: 142/255, green: 142/255, blue: 146/255, alpha: 0.5)

    }
    
    class var Black: UIColor {
        return UIColor(red: 29/255, green: 29/255, blue: 38/255, alpha: 1.0)
    }
    
}
